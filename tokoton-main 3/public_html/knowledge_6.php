    <div id="content_right">
        <h5 id="knowledge_title"><span class="ttl1">いろは その<span>6</span></span> <span class="ttl2">本当にお得な車検を選ぶ方法とは？</span></h5>
        <dl>
          <dt>「本当にお得な車検を選ぶ方法とは？」</dt>
          <dd>
            <p><!--<img src="/images/knowledge/booking_car_inspection.jpg" alt="車検予約" /><br />-->車検がそろそろ切れるけど車検の受け方が分からない、車検場を決める基準が分からない！そんな方々の為にこのページでは車検の流れの説明や車検選びのコツを紹介しています。安さで選ぶだけではない、自分に合った車検先を見つけてください。</p>
                               <div class="button-wrap">
                  <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
                </div>
          </dd>

          <dt>車検予約の流れ</dt>
          <dd>
            <p>初めに車検の大まかな流れについて説明します。今回は大多数の人が利用するであろう車検、整備の両方を行ってくれるガソリンスタンドや整備工場などの車検の流れについてのみ扱います。</p><br>
              <p><span>車検の見積もり予約</span></p>
              <div>
                <p>まずは複数のお店に車検の見積もりの予約をお願いしましょう。一番初めのステップですが一番大事なポイントと言っても過言ではありません。後述する車検先の選び方を参考に大体2.3件に見積もりを予約しておくと適切な委託先が選びやすくなります。</p>
              </div>
              <br />
              <p><span>実車による車検の見積もり</span></p>
              <div>
                <p>見積もりを予約した先で実際に車を見せて、車検時の見積もりを出してもらいます。その際、点検をしている整備士に同行して車の状態について理解しておくとその後の見積もりの透明性が高くなり、必要不必要の分別が付きやすくなります。</p>
              </div>
              <br />
              <p><span>見積もり確認、委託先の決定</span></p>
              <div>
                <p>見積書を吟味し、本当に必要な整備をしてくれるところ、安いところなど自分の設定していた予算等の条件をもとに委託先を決めましょう。整備士は万が一でも車が事故を起こさないように万全の整備を心がけているので優先順位の低い整備が見積りに入っていることもあります。見積もりをお願いする際に整備士に予算を伝えてみると整備の優先順位と予算を参考に見積りを決めてくれたりもするのであるのでお勧めです。</p>
              </div>
              <br />
              <p><span>入庫、検査、引取り</span></p>
              <div>
                <p>委託先を決めたら車検の予約をし、当日車を入庫させましょう。その後は業者が整備、点検を行い車検に通してくれます。委託先がその場で車検を行うことのできる「指定工場」であれば当日に車を返してもらうこともできます。しかし、通常の車検には1~3日かかってしまうので、その際の代車が必要であれば手配しておくとよいでしょう。</p>
              </div>
          </dd>



          <dt>車検見積もり先の決め方</dt>
          <dd>
            <p>
              車検を予約する流れの中でユーザーが考えて決めなければならないのは見積先と委託先ですが、委託先については見積書という明確な比較対象があるため選ぶことはさほど困難ではないでしょう。しかし、どこに見積もりをお願いするかについては明確な基準がありません。業者により車検で力を入れているポイント(整備の充実さ、費用等)はさまざまです。それらを基準に選ぶことも出来なくはないでしょう。しかし、費用に関していうとメンテナンス費用がいくらになるのかは店舗によって、はたまた整備する人によって変わってくるので見積もりを出してもらうまでどこが安いのか分からないのが現状です。<br/>
              そこで重要になってくる車検選びの最大のポイントは<br>
              <span class="sub">「安く車検に出すことではなく、安さも一つの項目に含めてお得に車検を行うこと」</span><br>
              です。
              お得に、と言われてもピンとくる方は少ないと思うので以下に車検を選ぶにあたって参考にすべきポイントをいくつか紹介していくので、自分にとって嬉しいオプションを考えてみてください。
            </p>
            <div class="button-wrap">
              <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
            </div>
            <br />
              <p><span>自宅からの近さ</span></p>
              <div>
                <p>ある意味安さ以前のポイントかもしれない大事な要素です。安い車検場でも遠いと手間がかかってしまうのでお得とは言えません。まずは自宅周辺で自分が車を持っていける範囲の車検場をピックアップしましょう。</p>
               </div>
               <br />
              <p><span>車検に出している間に使用する代車</span></p>
              <div>
                <p>車検の多くは１日以上かかってしまうことは説明しました。毎日のように車を使っている人であれば車検にかかる日数が２、３日であっても代車が必要でしょう。安くて近い車検場にお願いしたとしても車検の際の代車は遠い所でお金を払ってレンタカーを借りなければならない、となってしまってはお得ではありません。現在ほとんどの車検場で代車のサービスは行っていますが行っていない所もあるので注意する必要があります。また、有料での貸し出しのところもあるので詳しくは各店舗に問い合わせるのが良いでしょう。</p>
              </div>
              <br />
              <p><span>車検費用をクレジットカードで決済ができるか</span></p>
              <div>
                <p>車検はいくら安い所にお願いしても決して安い買い物とは言えません。現金で払うのは経済状況的に厳しいという人であればクレジットが使用可能であるという条件も大事になると思います。また、大きな買い物だけに多くのポイントが付く点も魅力的です。<br/>
※クレジットでの精算でも車検の費用の内、法定費用分はポイントが付かない場合や法定費用分だけ現金精算という車検屋もあるので注意が必要です。
</p>
              </div>
              <br />
              <p><span>土日、夜間受付</span></p>
              <div>
                <p>平日の昼間に車検に出す余裕がないほど忙しい人であれば気になってくるのが土日、夜間受付が可能かどうかでしょう。特に民間の整備場は土日がお休みのところも少なくありません。また、土日の内に車を返してほしいのであれば一日で車をすぐに返却してくれるようにするため、車検場が指定工場でなければならないので確認が必要です。その場で車検を行う資格を持っている指定工場以外の車検業者は国が管理している運輸局、運輸支局という場所に持って行き車検をお願いしていますが、運輸局は土日がお休みのため土日に車を持って行っても車検に通すのが月曜日以降になってしまうからです。</p>
              </div>
          </dd>
          <dt>まとめ</dt>
          <dd>
            <p>本当にお得な車検を予約するためには、安さだけではなくオプションなどにも注目する必要があります。たくさんのお店に実際のメンテナンス費用などの見積もりを取ることができれば良いのですが実際には車を持って行って点検を実施して、というのを何度も繰り返すのは大変なことです。安さ以外に重視するポイントで見積もり先を絞ることで効率よく自分に合った車検を見つけることができるでしょう。</p>
          </dd>
        </dl>

