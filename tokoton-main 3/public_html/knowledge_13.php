          <dt>車検が切れたときの罰則・罰金はある？運転したらどうなる？</dt>
          <dd>
              <p><span>車検切れを放置したらどうなる？</span></p>
              <div>
                <p>車検が切れて放置しているだけでは特に処罰といったペナルティはありません。しかし、車検が切れた車を所持していると様々なデメリットやリスクが潜んでいます。これらをきちんと理解し、ペナルティを受けないためにも対処の方法を確認しておきましょう。</p>
               </div>
               <br />
              <p><span>車検切れを運転したらどうなる？</span></p>
              <div>
                <p>車検切れの自動車を公道で走行すると法律違反となり、罰則を受けます。刑事処分として罰金や懲役刑、行政処分として運転免許への点数加点が与えられます。<br />
                車検が切れた場合の罰則にはどのようなものがあるでしょうか。</p>
                <br />
              <p><span class="sub">車検が切れた自動車を公道で走らせた場合のペナルティ(無車検車運行：道路運送車両法違反)</span></p>
              <p>違反点数6点(前科がない場合)<br />
              30日間の免許停止<br />
              6か月以下の懲役または30万円以下の罰金</p>
              <br />
              <p><span class="sub">自賠責保険が切れている自動車を公道で走らせた場合のペナルティ(自動車損害賠償保障法違反)</span></p>
              <p>違反点数6点(前科がない場合)<br />
              1年以下の懲役または50万円以下の罰金</p>
              <br />
              <p><span class="sub">車検切れで自賠責保険も切れた自動車を公道で走らせた場合のペナルティ</span></p>
              <p>違反点数12点(前歴がない場合)<br />
              90日間の免許停止<br />
              1年6か月以下の懲役または80万円以下の罰金</p>
              <br />
              <p>以上にまとめた通り、車検切れの車を公道で走行すると刑罰が与えられます。<br />
              車検切れから長い月日が経っているにもかかわらず公道を走行するなど悪意や常習性が認められた場合、逮捕されてしまうこともあります。</p>
              </div>
               <br />
              <p><span>車検切れで事故を起こしたら？多額の賠償金を払うことになるかも…</span></p>
              <div>
                <p>もし車検が切れた状態で走行し、交通事故を起こしてしまった場合はどうなるでしょうか？<br />
                車検が切れている場合は、自賠責保険も切れていることがほとんどです。<br />
                まず自賠責とは、強制加入の保険です。対人事故の際に被害者に対して保険金が支払われ、保険金額は死亡時3000万円、後遺障害時4000万円（最大）、傷害120万円(最大)となっています。<br />
                もし自賠責保険が切れている状態で事故を起こすと、これらの保険金額を事故を起こした側が支払わなければなりません。また、任意保険に加入していたとしても、車検切れでは事故の補償に適用されないことが多いです。<br />
                車検が面倒だと思っても、車検や自賠責保険が切れた状態で公道を走行することは絶対にやめましょう。</p>
              </div>
              </dd>

          <dt>車検切れの車を所持するデメリットとリスクとは？</dt>
          <dd><p>では、車検が切れてしまったときのデメリットやリスクにはどのようなものがあるでしょうか。</p><br />
              <p><span>税金がかかる</span></p>
                <p>車検切れでそのままクルマを保管する場合、自動車の税金、つまり車税が免税になることはありません。毎年、重量税などの税金の納税義務が生じます。もし一時的に車を使用しない場合は、運輸支局という役所で「一時抹消登録」の手続きを行うのがおすすめです。この手続きをすることで、税金の課税を止めることができます。</p>
            <br />
              <p><span>名義変更ができない</span></p>
                <p>道路運送車両法第13条第2項によると、車検が切れている状態では名義変更ができない決まりになっています。<br />
                例えば個人間で車を売買した場合、名義人の変更ができないことで自動車税の請求は元の持ち主のところへいってしまいます。</p>
            <br />
              <p><span>免許停止・はく奪</span></p>
                <p>上記に述べたように、車検が切れた状態で公道を走行すると、罰則、罰金といったペナルティが与えられます。過去に前歴がない場合は違反点数が ~12点の加点となり、免許停止期間も設けられます。悪意や常習性がある場合は一発で免許がはく奪されることもありますので注意しましょう。</p>
          </dd>

          <dt>車検切れのときの対処の方法その１　車検を受ける場合</dt>
          <dd>
            <p><span>仮ナンバーを取得する</span></p>
              <p>自分で運転して車検を行いたい場合は、市役所や区役所などの役所や、役場などの場所で「仮ナンバー」を取得することで臨時的に公道を走ることができます。<br />
              しかし、自賠責保険が切れていると仮ナンバーを発行することができないので注意しましょう。もし自賠責保険も切れているのであれば、自賠責保険に加入してから仮ナンバーを交付してもらいに行きましょう。自賠責保険は短期間から加入することができます。<br />
              この仮ナンバー交付手続きは、車検業者に代行してもらえるところもあります。忙しくて時間が取れない場合は、このようなサービスを利用するのも良いでしょう。<br />
              自分で行う場合、仮ナンバーの有効期間は通常3日程度、最大でも5日となっており非常に短いことに注意が必要です。仮ナンバーを取得してすぐに車検業者のところに行けるようにスケジュール調整をしておくことも必要です。<br />
              仮ナンバー発行手数料は市町村により異なりますが、750円程度です。</p><br />
            <p><span>引き取り納車を利用する</span></p>
              <p>引き取り納車とは、業者が自宅などに車を引き取りに来て車検を行い、納車するサービスです。キャリアカーやレッカー車、ローダー車などで移動させるため、車検が切れていても移動させることができます。また、利用者には時間的な縛りがないこともメリットです。<br />
              ただし引き取り納車を行っていない業者もあるため、引き取り納車が利用可能か、車検が切れていても対応してもらえるか、事前に業者に確認しておきましょう。そして、合計の費用の目安について査定額を出しておくことも大切です。中には、車検サービスの一環として無料で行っている業者もあります。<br />
              仮ナンバーを所得して自走する場合はトラブルやリスクを抱えているため、引き取り納車を利用することおすすめします。<br />
              <br />
              (とことん車検ナビでは、引き取り納車OKの業者をオプションで絞って検索することができます。ぜひ活用してみてください。）</p>
                <div class="button-wrap">
                  <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
                </div>
          </dd>

          <dt>車検切れのときの対処の方法その２　車を手放す場合　</dt>
          <dd>
            <p><span>廃車にする</span></p>
              <p>実は車検切れの車も廃車にすることが可能です。一時的に車を使用せず、放置しておくだけの場合や今の車を買ってから10年以上経つ場合、車が故障している場合は、廃車として処分してしまうのも1つの手です。<br />
              車検切れの車を放置しているだけでも税金はかかってしまいますし、車の寿命は10年ほどといわれており、それを超えると、たとえ修理をしたとしても故障のリスクは高まるという理由があります。また、修理すると上乗せされたお金がかかってしまうという理由もあります。<br />
              それでは、車検切れの車を廃車にする手順はどのようになっているでしょうか？その内容を見ていきましょう。<br />
              <br />
              まず、解体業者に廃車にする車を解体してもらいます。この時に注意しなければならないことは、解体業者に車を運ぶ際も車検切れの車を公道で走行させることができません。そのため、上記の「車検切れのときの対処の方法1」で挙げた、役所で仮ナンバーを所得するか、業者を手配し、レッカー車で運んでもらうことを検討しましょう。<br />
              車を解体した後に、普通自動車の場合は陸運局という役所で、軽自動車の場合は軽自動車検査協会で廃車手続きを行います。<br />
              ただし、車検証がなければ廃車にすることができません。車検証は再発行することが可能です。必要書類をもって役所に行きましょう。<br />
              車検証の再発行手続きは廃車手続きと同様に、普通自動車の場合は陸運局という役所で、軽自動車の場合は自動車検査協会で行います。<br />
              車検の紛失について、詳しくはこちらをご覧ください。<br />
              <a href="knowledge_9.php">車検のいろは その9「車検証を紛失した場合はどうすればいいの？」)</a></p><br />
            <p><span>売却する</span></p>
              <p>車検切れの車でも、中古車販売店などに売却することができます。車検が切れる前から売却を考えていたのであれば、売ってしまうという選択肢があることも念頭に入れておきましょう。<br />
              その場合は買取金額の査定を事前に見積もりましょう。</p>
          </dd>

          <dt>車検切れの取り締まりが厳しくなっている</dt>
