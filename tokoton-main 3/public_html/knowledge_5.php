            <p>車検の有効期間で勘違いしがちなポイントをまとめました。</p>
  　　　　　　 <div>
                  <span>中古車の車検期間</span><br>
                  <p>中古車の車検は初回さえ無事に終わってしまえば、その後は新車で購入した場合の2回目以降の期間と同じになるので間違えることは少ない
                  かと思います。しかし、購入時の車検の状態によっては「1年も乗っていないのに車検が切れてしまっていた」　なんてこともあり得るので初回
                  の車検には十分注意しましょう。<br>
                  中古車は購入時の車検の状態によって大きく三つに分けられます。</p>
                  <ul>
                    <li>車検2年付き、車検切れ</li>
                    <p>購入時に車検が切れているのが車検切れ、購入時に車検を通す費用を負担してくれるのが車検2年付きです。これら二つについては、2回目
                    以降の車検を行った状態と考えて良く、その後の車検は2年→4年→6年…というふうに続くのでそこまで問題ないかと思います。</p>
                    <li>車検残り</li>
                    <p>中古車の車検で陥りやすいのがこの車検残りです。購入時に車検が1年近く残っている場合などに、残っている車検の有効期限をそのまま
                    で販売いることがあります。その場合、当然購入者は車検が切れる前に車検を通さなければならないので、初回の車検がいつ訪れるのかはし
                    っかり確認してから購入するようにしましょう。</p>
                  </ul>
               </div>
