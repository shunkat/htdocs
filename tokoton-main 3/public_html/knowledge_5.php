          </dd>
          <dt>車検を予約する時期</dt>
          <dd>
            <p><img src="/img/user/knowledge/calendar.jpg" alt="カレンダー" width = "557" height="370"><br>
            車検は1ヶ月前から損をせず受けられると説明しましたが、実際に検査をする何日前に予約すればいいかを説明していきます。
            </p>
  　　　　　　 <div>
                  <span>予約時期と早割</span><br>
                  <p>車検業者は入庫の1週間~1ヶ月前から予約を受け付けているところが多いです。<br>
                  しかし、車検業者によっては早くから予約することで料金の割引が受けられる「早割制度」というものを導入しており、1週間前、1ヶ月前、3ヶ
                  月前など予約のタイミングが早くなるにつれて割引率も高くなる場合があります。<br>
                  3ヶ月前ともなると予定を組むのが難しくなってくるため、<span class="sub">早めから車検を意識して予定を組むことがお得に車検を受けるためのコツ</span>と言えるでしょう。<br>
                  早割制度ですが、もっとも早くから車検の予約を行っている業者はなんと2年前(新車であれば3年前)から予約を受け付けています。予約と言っ
                  ても、当然2年後の予定など分からないので日にちは確定させない仮予約と言う形式が多いようです。車検を受ける場所が決まっている場合は利
                  用してみると良いでしょう。</p>
               </div>
          </dd>
          <dt>車検が切れた場合の罰則</dt>
          <dd>
          	<p>車検が切れること自体に違反性はありませんが、車検が切れた状態で車を公道で走らせてしまうと法律違反となります。<br>
2016年秋には国土交通省から「車検が切れ車両をその場で摘発できるシステムを2017年度より実験的に導入する」という発表がありました。<br>
国土交通省によると全国で約20万台もの車が車検切れの状態で公道を走行しているそうです。仮に、車検も自賠責保険も切れた状態で公道を走行したことが発覚した場合、一発で</p>
<ul>
<li>違反点数12点</li>
<li>90日間の免許停止</li>
<li>1年6ヶ月以下の懲役または80万円以下の罰金</li>
</ul>
<p>が科せられます。公道を走行しただけでこの処罰ですから、事故でも起こした場合には目も当てられません。<br>
車検が切れた場合の罰則としては、以下が考えられます。</p>
		  		  <div style="margin-top: 15px">
                 <span>車検が切れた車を公道で走らせた場合<br>
(無車検車運行：道路運送車両法違反)</span>
                    <ol>
                    <li>違反点数6点(前歴がない場合)</li>
                    <li>30日間の免許停止</li>
                    <li>6ヶ月以下の懲役または30万円以下の罰金</li>
                    </ol>
                    </div>
                    <div style="margin-top: 15px">
                <span>自賠責保険が切れている車を公道で走らせた場合(自動車損害賠償保障法違反)</span>
