                  <span>10年経過車の車検期間</span><br>
                  <p>10年経過車は毎年車検が必要になる、と聞いたことがあるかもしれません。しかし1995年の道路運送車両法の改正によって10年経過車の車検
                  期間も2年のままとなりました。
                  技術の発達から車の寿命は年々増加傾向にあり、現在では小型車、普通車ともに寿命が12年を上回っています。
                  </p>
               </div>
