        <dl>
          <dt>「いつから受けられるの？車検の期間について」</dt>
          <dd>
              <p>自動車をお持ちの方なら誰しもが経験する車検。<br>
                  皆さんは車検の有効期間について、「フロントガラスのシールに記載されている月になったら予約すれば大丈夫」と思っていませんか？<br>
                  ここでは、車検の期間の調べ方から推奨する予約時期、車検時に気をつけるポイントまで、車検に関して気をつけたいあらゆるポイントについてまとめてお伝えしていきます。
              </p>
              <div class="button-wrap" style="width:70%;margin:20px auto;">
                  <a href="#area_text" class="search_link_button" style="padding:15px 0;background:#fc571f;">お住まいの地域から車検を探す</a>
              </div>
          </dd>

          <dt>車(普通車・軽自動車・バイク)の車検の有効期間</dt>
          <dd>
            <p>車検の期間は車の種類(普通車・軽自動車・バイク)に寄らず基本的に全て同じです。<br>
しかし主に貨物車に区分される1・4ナンバーの車だけは車検期間が他と異なるので注意が必要です。</p><br>
                  <span>新車3年、それ以後2年</span><br>
                  <p>新車の場合、有効期限は登録日から3年です。たとえば2015年11月3日に車両の登録を行った場合は車検の有効期限は2018年11月2日ま
                  でとなります。<br>
                  2回以降の車検の有効期限は2年です。なので、車検の時期は新車の3年目車検から5年目→7年と言う順に訪れます。</p>

               <div style="margin-top: 15px">
                  <span>1・4ナンバーの車検の有効期限</span><br>
                  <p>
                  前述した通り、1・4ナンバーは他の車と車検の時期が異なります。1ナンバーの車は普通貨物車、4ナンバーの車は
                  小型貨物車・軽貨物車に区分され、重い荷物を輸送する用途で使われることが多いのでその分車検の期間は短くなっています。<br>
                  1・4ナンバーの普通車の車検の有効期限は新車なら2年、その後の車検は1年となります。
                  なので、車検の時期は2年→3年→4年と言う順に訪れます。<br>
                  4ナンバーの軽自動車(軽自動車に1ナンバーは無い)の車検の有効期限は
                  初回、2回目にかかわらず2年となっているので、車検の時期は2年→4年→6年と言う順に訪れます。
                  </p>
               </div>
          </dd>
          <dt>車検はいつから受けられるのか、いつ受けるべきか</dt>
          <dd>
            <p>車検の有効期限について説明しましたが、実際に登録した日なんて覚えていない人がほとんどでしょう。<br>
            では有効期限は何を見て判断すればよいのでしょうか？</p>
    　　　　　　 <div>
                  <span>有効期限の調べ方</span><br>
                  <p>もっとも確実でお勧めできる方法は車検証を見ることです。<br>
                  <img src="/img/user/knowledge/certificate.jpg" alt="車検証" width = "557" height="370"><br>
                  上の車検証の写真左下にある「有効期間の満了する日」が車検の有効期限となるため、この日までに車検を更新しなければなりません。<br></p>
                 </div>
                 <div style="margin-top: 15px">
                  <span>満了日の判断に車検シールを見てはいけない！</span><br>
                  <p>
                  <img src="/img/user/knowledge/Sticker.jpg" alt="車検シール" width = "300" height="200" ><br><br>
                     <div class="button-wrap" style="width:70%;margin:20px auto;">
                         <a href="#area_text" class="search_link_button" style="padding:15px 0;background:#fc571f;">お住まいの地域から車検を探す</a>
                     </div>
                  <p>車検の有効期間を調べる際に車検証を確認するのが良いと説明しましたが、フロントガラスに貼り付けられた車検シールでも有効期限がわかる
                  のではないか、という人もいるかと思います。(上写真)<br>
                  しかし、フロントガラスの表(外側)に書いてあるのは車検が満了する年月だけなのです。つまり車検シールに28年7月と書いてあったとしても有
                  効期限が7月1日なのか31日なのかまではわかりません。車検を受けたことのない人が末日まで大丈夫だろうと思っていたらいつの間にか満了日
                  を過ぎていたなんてことや、末日ではないと知り、期限を確認した時には都合の合う日がなく、オプション代金を支払って引き取りに来てもら
                  うハメになった、なんて事もあり得ます。<br>
                  車検シールの裏には日付まで書いてあるものもありますが表同様月までしかわからないものや手書き
                  で日にちを記入する車検シールもあり、満了日を勘違いしてしまう可能性があります。そのような自体にならないためにも車検シール等を見て
                  車検が近づいてきたと感じたら満了日は車検証で確認することが大事なのです。</p>
                 </div>
                 <div style="margin-top: 15px">
                  <span>車検シールは貼っていないと罰金</span><br>
                  <p>
				 車検シールを貼ることは、車検証の車載と共に道路運送車両法によって義務付けられています。<br>
				 これに違反した場合、<span class="sub">【50万円以下の罰金】</span>が科されることもあります。<br>
				 貼り付け場所についても規定がありますので、事前に調べて間違いのないようにしましょう。
				 </p>
                 </div>
                 <div style="margin-top: 15px">
                  <span>継続検査申請書による発行</span><br>
                  <p>
				 車検シールは、継続検査申請書による申請で発行することができます。<br>
				 手続き当日には、その他に
                 <ul>
                 <li>自動車検査証の原本</li>
                 <li>自動車検査証に記載の使用者の印鑑</li>
                 <li>検査標章（毀損などで提出できる場合は、提出が必要です）</li>
                 が必要です。
                 </ul>
                 <p>手続きについては、最寄りの事務所・支所・分室にて申請となります。
				 </p>
                 </div>
                 <div style="margin-top: 15px">
                  <span>車検の満了日1ヶ月前から受けるのが「基本」</span><br>
                  <p>いつまでに車検を受けなければならない、と言う期限はありますが、実は車検を受けることはいつでも可能です。極端な例を挙げると車検に
                  合格した1週間後にもう一度車検を受けることも出来ます。<br>
                  しかし、その場合車検の有効期間は最後に合格した日から2年間となるため車検の回数をなるべく抑えるために期限が近づいてから受ける人がほ
                  とんどです。<br>
                  「車検の有効期間が車検に合格した日から2年間ならば、満了日に車検を受けるのが一番お得なのでは？」と思う人もいるかもしれません。<br>
                  しかし、混雑回避や仕事で忙しい人がいることを踏まえて、満了日の1ヶ月前以降であればその車検の有効期間が満了日に車検を受けた場合と同
                  じ期間になります。具体的には、2016年12月15日が車検の満了日の場合は2016年11月15日以降に車検を受ければ次の車検満了日が2018年12月
                  15日となり、仮に2016年11月14日に車検を受けてしまうと、次の車検満了日は2018年11月13日となってしまいます。
                  そのため<span class="sub">車検の満了日1ヶ月前から受けるのが基本となっています。</span></p>
                 </div>
    　　　　　　 <div>
                  <span>45日前から受けられる特例もある</span><br>
                  <p>車検業者の中には、満了日の45日前以降の車検であれば先程述べたものと同じく満了日に車検を受けた場合の期間と同じ有効期間になる業者
                  もあり、そのことを強みにしている業者もあります。これには保有している工場に理由があります。<br>
                  車検業者には大きく分けて下の2種類の業者がいます。
                  <ul>
                    <li>認証工場：整備のみを行える。検査自体は他の検査場で行う。</li>
                    <li>指定工場：整備から検査までを一貫して行う事が出来る。</li>
                  </ul>
                  <p>指定工場の場合、車を検査場に持っていくことなく自分の工場で検査を実施できます。ただし検査実施後15日以内に車検場に書類を提出し、正
                  式な車検証を発行する必要があります。そのため45日前に車検を実施し、その後15日後に車検場に書類を提出してもらうことで、45日前から車
                  検を実施することができます。<br><br>
                  もし車検をする工場が指定工場ならば、45日前から車検ができないか相談してみると応じてくれる可能性があるでしょう。</p>
                 </div>
