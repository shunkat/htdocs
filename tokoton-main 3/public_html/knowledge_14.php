    <h5 id="knowledge_title"><span class="ttl1">いろは その<span>14</span></span> <span class="ttl2">車検ローンってなに？ローンの種類と審査基準！</span></h5>
        <dl>
          <dt>車検ローンってなに？ローンの種類と審査基準！</dt>
          <dd>
            <p><!--<img src="/img/user/knowledge/knowledge15.jpg" alt="車検ローンってなに？ローンの種類と審査基準！" width="557" /><br />-->
            車検代を一括で支払うのが難しいことはありませんか？<br />そんな時に分割払いをする方法が車検ローンです。ここでは車検ローンの種類や審査基準などを詳しくご紹介します。</p>
          </dd>

          <dt>車検ローンの種類</dt>
          <dd>
           <p>車検のローン支払い方法はいくつかあります。金利やローン審査時間には差があります。<br />
               ここではそれぞれの特徴をご紹介します。</p><br />

            <p><span>マイカーローン</span>（全額一括）</p>
            <p>通常マイカーローン(自動車ローン)は車購入費用のローンです。ですが修理費用や車検費用にも利用できるローンがあります。
            金利はおよそ1％～5％と最も低くなっています。ただ審査時間が長く、審査も厳しいので計画的に申請する必要があります。</p>
            <br />
            <p><span>金融機関などのフリーローン</span>（全額一括）</p>
            <p>銀行フリーローンの金利はおよそ2％～15％とマイカーローンに比べ高めです。審査時間は2週間程度かかります。また、マイカーローン程ではありませんが審査も厳しいです。そのため、お急ぎの方にはおすすめできません。</p>
            <br />
            <p><span>クレジットカードによる支払い</span>（全額一括）</p>
            <p>ローン支払いとは異なりますが、クレジットカードでの分割払いが可能です。
            金利はおよそ8％～18％ですが、分割回数やカードの種類で変動します。
            また、クレジットカード未対応の店舗もあるので事前確認が必要です。</p>
            <br />
            <p><span>車検をする店舗でのローン</span>（業者別）</p>
            <p>信販会社と提携して車検ローンに対応している店舗もあります。金利はおよそ5％～15％と高めになっています。しかし他のローンに比べ、審査時間は短く審査に通りやすいです。
            全額ローンを組めるかは業者により異なります。詳しくは次の記事で解説しています。</p>

          <dt>店舗での車検ローン詳細</dt>
          <dd>
           <p>業者別車検ローンを知る上で車検費用の内訳を知ることが大切です。</p>
           <p><img src="img/user/knowledge/knowledge_14_2.jpg" alt="業者別車検ローンを知る上で車検費用の内訳を知ることが大切！" width="100%" height="" class="border" /> <br class="clear" /></p><br />
            <p><span>車検費用とは</span></p>
            <p>車検費用は車検基本料と法定費用の総額です。<br />
                車検基本料：点検や代行など業者の整備サービスであり業者ごとに値段が異なります。<br />
                法定費用：国に支払うお金なので全国一律の金額です。（重量税、印紙代、自賠責保険料）<br />
                詳しくは<a href="knowledge_7.php">いろは　その７</a>をご覧ください。</p>
            <br />
            <p><span>なぜローンを組める幅に差がある？</span></p>
            <p>法定費用は国に支払うお金なので原則現金支払いです。そのため、ローンに含まない業者があります。一方でローンに含む業者もあります。この違いによりローンを組める幅に差が生じます。<br />
                <span>＊先の記事で解説した「車検をする店舗でのローン」以外は金融機関に車検費用を全額借りるので一括払い、金融機関に全額ローンになります。</span></p>
            <br />
            <p><span>車検業者別車検ローンや支払い方法の特徴</span></p>

            <table class="type01">
            <tbody><tr>
