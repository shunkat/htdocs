          <dt>車検のローン審査基準</dt>
          <dd>
        <p>車検ローンにはローン審査があります。審査では返済能力があるかを審査しています。<br />
        審査に通るように審査基準を確認していきます。</p><br />
        <p><span>収入が安定しているか、職業、勤務年数など</span></p>
        <p>基本的には企業に就職していれば問題はありません。３年以上の勤務であると安心です。
        アルバイトや自営業は収入が不安定とみなされ、ローン審査に通らないことがあります。また高収入の場合でも不安定と判断され通らないこともあります。</p>
        <br />
        <p><span>過去に延滞情報があるか</span></p>
        <p>ローン利用申込みを行うと信用情報機関に情報の照会が行われます。延滞情報は延滞日より1～5年間残ります。そのため心配な場合は信用情報機関に開示請求を行い確認してください。開示請求は1000円程度で行えます。リボ払い、奨学金、携帯電話などの延滞も延滞情報として記録されるので注意が必要です。</p>
        <br />
        <p><span>クレジットカード借り入れが多いか</span></p>
        <p>大半の個人向け融資限度額は年収に対する3割までとなっています。
        他会社からの借り入れも含み、多額の場合返済能力なしとみなされ審査に通りません。
        延滞同様にリボ払い、奨学金、携帯電話なども返済額とみなします。またキャッシング枠がある場合未使用でも借入額に含まれます。未使用のキャッシング枠は解約しておくとよいです。</p>
        <br />
        <p><span>審査書類記入ミスがある場合</span></p>
        <p>記入ミスがある場合、ローン審査に通ることはないです。記入後、書類に間違いがないか確認しましょう。</p>
          </dd>

          <dt>車検のローン審査落ちしたときの対処法</dt>
          <dd>
           <p>車検ローン審査落ちた‼車検費用を払えない‼そんなピンチな時の対処法をご紹介します。</p>
           <p><img src="img/user/knowledge/knowledge_14_3.jpg" alt="車検のローン審査落ちしたときの対処法" width="100%" height="" class="border" /> <br class="clear" /></p><br />
            <p><span>銀行などのカードローン</span></p>
            <p>フリーローンに比べ金利は高くなりますが、審査基準が緩く融資までが速いです。</p>
            <br />
            <p><span>消費者金融フリーローンやカードローン</span></p>
            <p>消費者金融とはアコムやアイフルなどがあります。
            金利は高くなりますが審査時間は短いです。早いものであれば30分で審査が通ります。
            もう残り日数が少ない場合は使用を考えましょう。</p>
            <br />
            <p><span>クレジットカードのリボルビング払い</span></p>
            <p>金利の相場はおよそ15％と高いです。どうしても月払いにしたいときの対処法です。
            ただしクレジットカード作成には1週間程度かかります。</p>
            <br />
            <p><span>車検を受けるだけでローンを組める店舗を探す</span></p>
            <p>自社ローンを組んでいる業者の中には車検を受けるだけで、ローン条件を満たす業者があります。自社ローンを組んでいる業者であればブラックリストに載っている方や過去に自己破産をしている方でもローンを組めます。しかし、金利が高い場合もあるので契約の際は注意が必要です。</p>
          </dd>

          <dt>まとめ</dt>
          <dd>
           <p>今回は車検ローンの種類と審査基準についてご紹介しました。それぞれの車検ローンに長所、短所があるのでご自身に最適な車検ローンを見つけてください。</p>
          </dd>
        </dl>

