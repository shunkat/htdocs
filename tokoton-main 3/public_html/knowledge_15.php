          <dt>車検を安く抑える</dt>
          <dd>
           <p>車検費用を安く抑えるコツについて解説していきます。</p>
           <br />

            <p><span>割引などを利用する</span></p>
            <p>業者によっては早期予約などによる割引を行っています。<br />
                検討中に業者にどんな割引があるのか問い合わせてみてください。<br />
                詳しくは「<a href="knowledge_3.php">いろは　その3</a>」でご紹介しています。</p>
            <br />
            <p><span>事前に見積もりを出す</span></p>
            <p>複数の業者から車検見積もりを取り、最安の車検業車で車検をするというのもありです。<br />
                「<a href="knowledge_10.php">いろは　その１０</a>」では見積もり方法とメリットについて詳しく解説しています。</p>
            <br />
            <p><span>ユーザー車検</span></p>
            <p>ユーザー車検の場合は人件費・手数料がかからないため費用を抑えることが出来ます。<br />
                また、場所にもよって異なりますが点検のやり方が分からなければ誰かに聞くことも可能です。</p>
            <br />
            <p><span>点検項目を減らす</span></p>
            <p>整備項目が詳細に記載されている見積もりを提示してもらってください。<br />
                ユーザーにとって不必要な検査項目があります。例えばエアフィルタ交換などです。<br />
                不必要な項目は業者と相談のうえ外してもらいましょう。<br />
                次の記事では車検前にセルフチェックしておくと良い項目について紹介します。</p>
         </dd>

          <dt>車検前にチェックする項目</dt>
          <dd>
            <p>ここでは車検前にセルチェックしておくとよいものを紹介していきます。</p>
            <br />
            <p><span>エンジン周りの点検</span></p>
            <p>オイル類やウォッシャー液などの液体類をチェックしましょう。
                項目としてはエンジンオイル、冷却水、ブレーキフルード、ウォッシャー液です。</p>
            <br />
            <p><span>電気、点火装置の点検</span></p>
            <p>点火装置、バッテリー、電気配線などの接続状況が大丈夫かチェックしましょう。</p>
            <br />
            <p><span>電飾系の点検</span></p>
            <p>電球が切れてないかチェックしましょう。</p>
            <br />
            <p><span>ブレーキの点検</span></p>
            <p>ブレーキの効き具合だけでなくペダルやサイドブレーキ、ブレーキレバーの状態もチェックしましょう。</p>
            <br />
            <p><span>タイヤや足回りの点検</span></p>
            <p>ドレスアップしていなければ大丈夫です。ただ、ひび割れやスリップサインがないかチェックしましょう。</p>
            <br />
            <p><span>トランスミッション関連の点検</span></p>
            <p>油の漏れや油量がポイントです。特にトランスミッションとデファレンシャルの油漏れは慎重にチェックしましょう。</p>
            <br />
            <p>以上が事前にチェックしとくと良い箇所です。もしこの項目いらないなと感じた項目があれば車検費用を抑えるために点検を省くのもありかもしれません。また車検前にカー用品店で安く交換する方法もあります。</p>
            </dd>

          <dt>推奨整備</dt>
          <dd>
           <p>ここでは必ず整備しなくてもいいですが、結果としてやっといたほうがお得な推奨整備についてご紹介します。</p>
           <br />
            <p><span>エンジンオイル＆エレメント</span></p>
            <p>エンジンオイルは走行距離や時間とともに劣化します。交換時期は半年ごとです。<br />
                エレメントは	エンジンオイルの不純物をろ過するフィルターです。<br />
                整備を怠ると燃費の悪化や走行性能低下やエンジンの焼き付きの原因になります。<br />
                交換時期はエンジンオイル交換2回につき1回です。</p>
            <br />
            <p><span>ブレーキパッド</span></p>
            <p>ブレーキの効き具合に影響します。ブレーキを踏むたびすり減るため消耗品です。<br />
                交換時期は使用状況により異なります。</p>
            <br />
            <p><span>ブレーキフルード（ブレーキオイル）</span></p>
            <p>時間とともに劣化します。こちらもブレーキの効き具合に影響してきます。<br />
                交換時期は2年ごとです。</p>
            <br />
            <p><span>ラジエータークーラント</span></p>
            <p>エンジンの冷却水のことです。定期的な交換を怠ると冷却系に大きな損傷を与えてしまします。また交換すべきタイミングが見た目ではわかりにくくなっております。<br />
                交換時期は2年ごとです。</p>
            <br />
            <p><span>デフオイル</span></p>
            <p>デフオイルとはディファレンシャルギアという装置を動かすのに必要です。<br />
                交換を怠ると走行時の異音やディファレンシャルギアが破損の原因になります。また、ディファレンシャルギアは高額で破損した場合約20万円程度かかると考えてください。<br />
                交換時期は2年もしくは走行距離が約5万kmに達した時です。</p>
            <br />
            <p><span>バッテリー</span></p>
            <p>充電した電気を蓄え、電装品へ電力を供給します。<br />
                交換時期は2～3年ごとです。</p>
            <br />
            <p><span>点火プラグ</span></p>
            <p>エンジンを点火させる役割があります。劣化するとエンジンがかからなくなります。<br />
                交換時期は走行距離2万～4万kmごとです。</p>
            <br />
            <p><span class="sub">カー用品店などで交換するほうが安いもの</span></p>
            <p>タイヤ交換、オイル交換、オイルフィルター交換、バッテリー交換、エアコンフィルタ交換、エアフィルタ交換、発煙筒、ワイパー交換などがあります。<br />
                車検時に交換したほうが良いものもあるので状態を見て交換しましょう。</p>
         </dd>

          <dt>車検費用が高くなる？</dt>
          <dd>
           <p>車検費用が高くなるタイミングについてご紹介します。</p>
           <br />
            <p><span>古い車</span></p>
              <p>初度登録から13年以上たつと自動車税は約15％、重量税は約39％が高くなります。<br />
              また、１8年以上たつとさらに約10％程度高くなります。</p>
              <br />
              <p><span>走行距離が長い車</span></p>
              <p>走行距離が10万キロを超えると多くの部品交換をしないといけないので高額になってしまいます。<br />
              例えばエンジンベルト、タイミングベルト、ドライブシャフトなどです。</p>
              <br />
          </dd>

          <dt>車検支払方法</dt>
          <dd>
            <p><span>クレジットカード</span></p>
              <p>クレジットカードは車検基本料と整備費用の支払いで使用することが出来ます。しかし、原則、法定費用は現金支払いなのでクレジットカードを使用できません。利用は国に支払うお金だからです。なお業者によっては全額クレジットカード払い可能な業者もあります。</p>
              <br />
              <p><span>車検ローン</span></p>
              <p>車検時にお金がない場合車検ローンで支払うことが可能です。<br />
              ローンにもさまざまな種類があるのでご自身に合うローンを探してみてください。<br />
              ローンについてはこちらに記事で詳しく解説しております。</p>
              <br />
          </dd>

          <dt>外車・輸入車の車検代は高い？</dt>
          <dd>
           <p>外車の車検代は高いというイメージがありますよね。<br />
           ここでは実際にどうなのか、また理由についても解説していきます。</p>
           <br />
            <p><span>法定費用高くなる？</span></p>
            <p>外車だろうと法定費用が変わることはありません。しかし、外車は国産車に比べ重量が重いです。そのため、同等のサイズでも外車のほうが国産車に比べ重量税が高くなってしまいます。</p>
            <br />
            <p><span>高くなる原因は？</span></p>
            <p><span class="sub">研修代</span></p>
            <p>海外メーカーは新型車種が販売されるたびに研修を行っています。<br />
                営業や整備をするスタッフが車種ごとの特徴やセールス手法、ポイントを学びます。<br />
                研修には費用が掛かるので車検費用にも反映して高くなっています。</p>
            <br />
            <p><span class="sub">部品代</span></p>
            <p>まず、日本メーカーと海外メーカーでは車を作る上でのポリシーが違います。<br />
                国産車はオイル交換とバッテリー交換のメンテナンスなどでも、基本的にユーザーの使用期間である10年10万kmを問題なく走れるように作られています。<br />
                対して外車は3年、5年ごと、3万km・5万kmごと定期的に交換をすることで走行性能低下を回避するという思想がベースにあります。そのため国産車と比べ交換する部品が多くなっています。<br />
                日本車と違い部品を輸入しなければなりません。また保管などにも環境を整えるための費用が掛かります。</p>
            <br />
            <p><span class="sub">日本車にはエコカー減税がある</span></p>
            <p>外車にはエコカー減税がありません。法定費用が変わらなくとも減税が有無で費用に差が出てしまいます。そのため、外車は国産車に比べ割高に見えてしまいます。</p>
            <br />
            <p><span>ディーラーじゃなくても大丈夫？</span></p>
            <p>結論からはいるとディーラーが一番おすすめです。<br />
                ディーラーはそのメーカーの車種や生産国の知識があるからです。車はその生産国の道路事情や気候などを考慮し作られています。そのため、国産車とは構造やパーツの仕様が異なるケースがあります。ディーラーは技術だけでなく輸入車の国ごとの特徴も理解しています。加えて純正のパーツの交換や保証があるため、ディーラー車検が圧倒的に安心できます。</p>



          </dd>

          <dt>まとめ</dt>
          <dd>
              <p>車検費用の相場、相場にかかわる要因についてご紹介しました。安くなるコツや高くなるタイミングにはそれぞれ明確なポイントがあるので理解しておくことが大切です。</p>
          </dd>
