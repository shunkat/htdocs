    <h5 id="knowledge_title"><span class="ttl1">いろは その<span>18</span></span> <span class="ttl2">コロナで車検延長？注意点は？</span></h5>
        <dl>
          <dt>コロナで車検延長？注意点は？</dt>
          <dd>
            <p><!--<img src="/img/user/knowledge/knowledge12.jpg" alt="車検をしている様子" width="557" /><br />-->
            コロナで車検を受検できず焦っている方もいると思います。国土交通省は緊急事態宣言により車検の有効期限を延長することを発表しました。この記事では車検期間がいつまで延長したのか、次回の車検にどう影響するのかを詳しく解説していきます。</p>
          </dd>

          <dt>車検いつまで延長？</dt>
          <dd>
            <p><span>2/28～3/31までに車検が切れる方へ</span></p>
              <p>車検の有効期限が3月31日までの車は当初は有効期限が4月30日まで延長でした。<br />
              しかし6月1日までに有効期限が更新されました。<br />
              また自賠責保険（強制保険）も申請なしで6月1日まで延長になりました。<br />
              加えて、自動車検査証の変更手続きも不要です。</p>
              <br />
              <p style="color: red;">※７都府県の車両は4月30日から6月1日まで変更になりました。（4/7）<br />
                  全国の車両は4月30日から6月1日まで変更になりました。（4/16）</p>
                  <br />
            <p><span>4/8～5/31までに車検が切れる方へ</span></p>
              <p>車検の有効期限が5月31日までの車は有効期限6月1日まで延長されました。<br />
              また同様に自賠責保険（強制保険）も申請なしで6月1日まで延長になります。<br />
              自動車検査証の変更手続きも不要です。<br />
              <br />
              当初は緊急事態宣言の対象である7都府県（東京、埼玉、神奈川、千葉、大阪、兵庫、福岡）だけに特別措置が取られていましました。しかし政府が4月16日にこの措置を全国も対象にしました。</p><br />
            <p><span>4/1～4/7までに車検が切れる場合</span></p>
              <p>車検有効期間4/1～4/7までの車に関しては延長措置対象外となっています。そのため有効期限後に受検した場合、従来同様に車検切れになります。<br />
              車検切れの対処方法については「<a href="knowledge_13.php">いろは13 車検切れの場合の対処法</a>」の記事でご紹介していますのでご覧ください。</p>
