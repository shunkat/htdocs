          </dd>


          <dt>次回の車検・強制保険の有効期限は？</dt>
          <dd>
           <p>ここでは有効期限が変動することに次回の車検・強制保険の有効期限の調べ方を紹介しています。また注意点についても解説していきます。</p><br />
            <p><span>次回の車検有効期限はどうなる？</span></p>
              <p>特例措置に伴い国土交通省か次回有効期限が調べられる表が発表されたので記載します。<br />
              ご自身がどれに当てはまるかご確認ください。（ピンク：通常、青：特例措置）<br />
              ※事前に伸長手続きは不要です。継続検査時に車検の伸長希望の旨をお申し出ください。<br />
              <img src="img/user/knowledge/knowledge_18_1.jpg" alt="自動車検査証の伸長に係る有効期限について" width="100%" class="border">
              <br class="clear" />
              出典：国土交通省資料<br />
              <a href="http://www.mlit.go.jp/jidosha/kensatoroku/kensa/200416_shincho.pdf" target="_blank">http://www.mlit.go.jp/jidosha/kensatoroku/kensa/200416_shincho.pdf</a><br />
              <a href="http://www.mlit.go.jp/jidosha/kensatoroku/kensa/index.htm" target="_blank">http://www.mlit.go.jp/jidosha/kensatoroku/kensa/index.htm</a></p><br />
            <p><span>新しい自動車損害賠償責任保険の契約期間</span></p>
              <p>車検有効期限の延長を希望する・しない、有効期限切れ、車検の受検日により新しい自賠責保険の契約期間が変わってきます。<br />
                <br />
                例：自家用車　2年車検の場合<br />
                <span class="sub">（１）4月30日までの車検伸長を希望する</span><br />
                現自動車損害賠償責任保険の終期から新たな継続車検の有効期限の末日（令和4年4月30日まで）を補う自動車損害賠償責任保険を契約する<br />
                Ex) 現有効期限：3/30  車検日：4/10<br />
                新有効期限：令和2年3月30日～令和4年5月1日以降<br />
                <br />
                <span class="sub">（２）4月30日までの車検伸長を希望しないで受検日からとする場合</span><br />
                1 有効期限内で受検する場合<br />
                　従来のものと変わらない<br />
                2 現有効期限切れで受検する場合<br />
                　受検日から車検期間の末日までを補う自動車損害賠償責任保険を契約する<br />
                　Ex) 現有効期限：4/15  車検日：4/2<br />
                　新有効期限：令和2年4月15日～令和4年4月26日以降<br />
                <br />
                <span class="sub">（３）6月1日までの車検伸長を希望する</span><br />
                現自動車損害賠償責任保険の終期から新たな継続車検の有効期限の末日（令和4年6月1日まで）を補う自動車損害賠償責任保険を契約する<br />
                Ex) 現有効期限：4/15  車検日：5/19<br />
                新有効期限：令和2年4月15日～令和4年6月2日以降<br />
                <br />
                <span class="sub">（４）6月1日までの車検伸長を希望しないで受検日からとする場合</span><br />
                1 有効期限内で受検する場合<br />
                　従来のものと変わらない<br />
                2 現有効期限切れで受検する場合<br />
                　受検日から車検期間の末日までを補う自動車損害賠償責任保険を契約する<br />
                　Ex) 現有効期限：4/15  車検日：5/14<br />
                　新有効期限：令和2年4月15日～令和4年5月14日以降
                </p><br />
            <p><span>任意保険には注意</span><br />
              <p>任意保険は契約が車検時期と同時ということが多いと思います。しかしこちらは延長措置とは関係なく更新の手続きが必要です。無保険にならないよう各代理店に自分がその保険に加入しているか確認をお願いします。</p>
