    <div id="content_right">
        <h5 id="knowledge_title"><span class="ttl1">いろは その<span>2</span></span> <span class="ttl2">安心できる車検を選ぶコツ</span></h5>
        <dl>
          <dt>安心できる車検を選ぶコツ</dt>
          <dd>
          <p>愛車の車検は信頼できる所におまかせしたいですよね？<br>
                 費用を抑えるために格安業者で車検を受けたとしても車検後すぐに故障してしまっては本末転倒ですし、安心はお金に換えられません。し
                 かし、安全な車検の見つけ方が分からず、途方に暮れている人も多いのではないでしょうか？
                 分からないからといってよく考えずに近場の安いところで受けてしまっては後々後悔するかもしれません。
                 このページではそのような方のために車検業者を選ぶ際のポイントをご紹介します！
          </p></dd>
          <dt>1.車検業者の種類を決める</dt>
          <dd>
          <p>
             車検は業者によって特徴が大きく異なるため、自分に合った業者を選ぶ必要があります。以下の表は、各業者のメリット・デメリットを簡単にまとめたものになります。<br>

            <table class="type01">
	<tr>
		<th scope="row">車検業者</th>
		<td>メリット</td>
        <td>デメリット</td>
	</tr>
	<tr>
		<th scope="row">ディーラー</th>
		<td>整備の質が高い。サポートが充実している。</td>
        <td>費用が高い</td>
	</tr>
	<tr>
		<th scope="row">フランチャイズ車検業者</th>
		<td>安定した品質で安く受けられる。</td>
        <td>事前予約が必須。</td>
	</tr>
	<tr>
		<th scope="row">民間整備工場</th>
		<td>比較的安く、質の高い整備をしてくれるところが多い。</td>
        <td>工場や人によって整備や施設の差が大きい</td>
	</tr>
	<tr>
		<th scope="row">ガソリンスタンド</th>
		<td>費用が安く、24時間受付が多い。ガソリン値引きなどガソリンスタンドならではのサービスが充実している。</td>
        <td>店舗によって整備の質が異なる</td>
	</tr>
	<tr>
		<th scope="row">ユーザー車検</th>
		<td>法定費用以外の費用がかからないため最安。</td>
        <td>日頃から整備をしていないと故障のリスクが高い。</td>
	</tr>
	</table>

            <br>
            一番信頼できる業者は、自社メーカーの知識が豊富で交換部品に正規品を取り扱うディーラー車検ですが、デメリットにも書いたとおり費用がかなり高くなってしまいます。自身の予算と<a href="http://www.tokoton-navi.com/knowledge_7.php">「車検の費用」</a>を参考に業者を決めると良いでしょう。</p>
          </dd>
          <dt>車検の内容を決める</dt>
          <dd>
          <p>
          車検業者が行っている点検・整備・内容については業者によっても変わりますが、工場の種類によっても大きく特徴が異なります。<br>
          以下の違いも参考に車検業者を決めましょう。<br><br>

          車検業者は大別すると
          <ul>
            <li><span>&nbsp;・指定工場</span></li>
            <li><span>&nbsp;・認証工場</span></li>
            <li><span>&nbsp;・代行業者</span></li>
          </ul>
          の三つに分類できます。<br><br>

          <p><span>指定工場</span></p>
          <div>
            指定工場は民間車検場ともよばれ、車検を自身の持つ工場で行うことのできる工場です。<br>
フランチャイズ、ディーラーの大部分と一部の民間整備工場、ガソリンスタンドが指定工場に当たります。
          </div>
          <p><span>認証工場</span></p>
          <div>
            認証工場は指定工場とは異なり、車の整備のみを行い車検は近くの運輸支局にて行う工場です。<br>
            整備も行う車検業者のうち、指定工場でない所は認証工場になります。
          </div>
          <p><span>代行業者</span></p>
          <div>
            車検代行業者はその名の通り車検を通すことだけを代行して行う業者であり、認証工場を持っていない、整備を行うことができない業者もあります。
          </div>

          それぞれの違いとして、
          <ul>
            <li>・指定工場の大半は一日車検(45,60分車検等)に対応している(後述)</li>
            <li>・指定工場は自身の持つ工場で車検をおこなうので他人に車を運転される事がない(認証工場と代行業者は近くの運輸支局まで業者が運転して車検を行います。)</li>
            <li>・認証工場や代行業者は時間費用が比較的安い(指定工場は維持費が高く書かかかるので、それが必要ない認証工場と代行業者は安く車検を行うことが出来ます。)</li>
            <li>・車検代行業者では24ヶ月法定点検をおこなわないところが多い</li>
          </ul>
          以上の点が挙げられます。<br><br>

          <p><span>24ヶ月法定点検とは</span></p>
          <div>
            24ヶ月法定点検とは、車検と共に義務化されている56箇所の点検のことです。<br>
            車検は現時点で走行可能かどうかをチェックする検査であるのに対して24ヶ月法定点検は次回の車検まで安全に走行できるかどうかをチェックする検査になります。
            車検、24法定点検はともに法律により義務化されていますが、24ヶ月法定点検は行わなくても処罰を食らうわけではありません。
            しかし、安全に乗るためには必要不可欠なものですので必ず行うようにしましょう。<br>
            代行業者以外の業者に車検をお願いする場合は、24ヵ月法定点検もセットで行ってくれるため別で頼む必要はありません。
          </div>

          これらの違いを理解することで、車検業者の中からさらに自分の希望に合った車検場を選ぶことができます。
          </p>
          </dd>
          <dt>店舗を調べる</dt>
          <dd>
          <p>
          車検業者の種類を決めたら近くにある店舗を調べましょう。<br>
          店舗のサイトには基本的に整備士の人数やオプションやサービスその他の強み等が書かれているため、細かい特徴を基に車検業者を選ぶことが出来ます。より深く雰囲気を知りたい場合は直接電話してみて対応をみてみるのもオススメです。<br>
          口コミを調べるのもいいですが、個人の性格や車の状態によって評価は大きく変わってきてしまうのであくまで参考程度にとどめておきましょう。
          </p>
          </dd>
          <dt>見積りを出す</dt>
          <dd>
          <p>候補が絞られてきたらweb見積りを行い、比較するのですがweb見積りでわかる値段はあくまで基本料であり、具体的に必要な整備は実際に店舗に持ち込みをして見積りをお願いしないとわかりません。<br>
            そのため、web見積りで一番安かった一つの所に決めてしまず、実車見積りを複数店舗に依頼して比較するのが良いでしょう。
            このようにして候補を絞っていき信頼できると思った店舗に車検をお願いしましょう。
          </p>
          </dd>
          <dt>45分車検・60分車検は安心？</dt>
          <dd>
          <p>
            フランチャイズの車検業者や一部のディーラーなどで45分車検・60分車検等の文字を見かけることがあると思います。<br>
            通常であれば業者に車を預けてから2,3日かかる車検が1時間程度で終わってしまうことに不信感を抱く人も少なくないでしょう。<br>
            60分車検の仕組みと安全性についてご説明します。<br>
            <p><span>仕組み</span></p>
            <div>
            車検業者が持つ工場は、整備のみを行える認証工場とその場で車検も行える指定工場の二種類があります。<br>
            認証工場は他の車検場に車検を受けに行く必要があるため、60分車検を行っている業者の大半は指定工場を持つ業者です。そのような指定工場を持つ業者が事前に予約を受け付けることで整備を行う時間を確保し一台のくるまに対して集中的に整備、車検を行っています。
            また、他の業者では一般にサービスとして行われているワックスがけや室内清掃、洗車などの作業をカットすることで1時間での車検を可能にしているのです。
            しかし、いくら60分車検と言っても車の整備内容によっては一時間以上、または一日以上かかってしまう事があります。そのため、必ずしも60分で終わるわけではないということを理解していないとなりません。
            </div>
            <p><span>安全かどうか</span></p>
            <div>仕組みの文章を読んで頂ければ分かると思いますが、早いからと言って整備が行われていないわけではありません。60分車検は必ずしも60分で終わるわけではないということを理解した上で、選択肢の一つとして考えると良いでしょう。
            </div>
          </p>
          </dd>
          <dt>まとめ</dt>
          <dd><p>車検を安全に行うには下調べが重要です。
          <br>とことん車検ナビでは全国の車検業者から自分に一番あった業者を見つけることが出来るので、ぜひ活用してください。
          わけもわからず近場の車検業者にだすのではなく、予算や店舗の情報を吟味して愛車にもお財布にも優しい車検を行い、楽しいドライブライフを！

          </p></dd>
        </dl>

