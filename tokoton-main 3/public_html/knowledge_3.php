        <dl>
          <dt>車検は安くなりました</dt>
          <dd>
          	規制緩和や企業間競争の影響もあって、車検は以前に比べて<span>格段に安くなりました。</span><br />
            また、消費者の眼も厳しくなってきたことで、例えば、勝手に整備をして、<br />
            後で多額の請求をしてくるようないかがわしい販売方法も減ってきました。<br />
             <span>料金の透明性が高くなった</span>と言えます。<br />
            <br />
            ただしご注意ください。企業努力によって<span>「激安」</span>を実現している車検は立派ですが、<br />
            <span>「激安」だけど手抜きだらけ</span>、という車検が存在するのも事実です。<br />
            価格と同時に<span>整備保証システムなどの確認</span>もお忘れ無く。
            <br>
                               <div class="button-wrap">
                  <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
                </div>
                </dd>

          <dt>お客様の協力次第でさらに料金が安くなる！</dt>
          <dd>車検には<span>工夫を凝らした「割引メニュー」</span>が採用されています。<br />
            割引きのパターンには<span>２種類</span>あります。<br />
            <br />
            <span>(１) 謝恩サービス型</span><br />
            <p>自社の車検に特徴を付けて注目していただくための割引サービスです。<br />
              もし該当する項目があればぜひ活用しちゃいましょう。</p>


            <br />
            例.<br />
            <ul class="exsample">
              <li>ＷＥＢ申込み割引</li>
                <li>創業10年記念割引</li>
                <li>車検実績1000台突破割引</li>
                <li>レディース割引</li>
                <li>シルバー割引</li>
                <li>リピート入庫割引</li>
            </ul>
              <br class="clear" />
            などなど。<br />
            <br />
            <span>(２) ご協力ありがとう型</span><br />
            <p>工場側が効率よく車検業務を進めるための協力をしてくれた場合に、<br />
              それを値引きで還元してくれるタイプのサービスです。<br />
              このタイプの割引メニューならばお客様側の協力次第で料金を節約できます。</p>
              <br />
            例.<br />
            <ul class="exsample">
              <li>前日入庫割引</li>
                <li>入庫日即決割引</li>
                <li>平日入庫割引</li>
                <li>代車なし割引</li>
                <li>早期予約割引</li>
                <li>早期入庫割引</li>
            </ul>
              <br class="clear" />
            などなど。</dd>

            <dd style="border:none;">


            </dd>


          <dt>とにかく安く済ませたいならば！</dt>
          <dd>車検は<span>「現在の車の状態」</span>が保安基準に適合したことを証明するものであって、<br />
            次の車検までの<span>安全性を保証するものではありません。</span><br />
            <br />
            したがって車検に<span>通すために必要な整備</span>と次回車検まで<span>安心して走行できる予防整備</span>とを<br />
            明確に提示してくれる車検工場こそが信頼できる工場と言えます。<br />
            <br />
            このような<span>予防整備を別料金にしている工場</span>と<span>車検料金に含んでいる工場</span>では、<br />
            当然のことながら<span>車検料金が大きく違います。</span>どちらを選ぶかはお客様ご自身のご判断です。<br />
            <br />
            しかし、予防整備が別料金の場合でも<span>信頼できる工場のメカニック</span>なら<span>丁寧に点検結果の説明をし</span>、<br />
            <span>どこを整備するかをお客様に選択させてくれる</span>場合もあります。<br />
            <br />
            特別な理由があって車検を安く済ませたい場合（車検後に売却するなど）は、<br />
            予防整備が別料金の工場を選んで、とにかく最低限の整備に抑えてもらえば<br />
            料金はかなり安くなるはすです。<br />
            <br />
            ただし、今後も長く乗り続ける車ならば、<span>メカニックのアドバイスに従って必要な予防整備を<br />
            車検と一緒にしっかり済ませておく</span>方が賢明です。</dd>
