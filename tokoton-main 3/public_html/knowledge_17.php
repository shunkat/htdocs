    <h5 id="knowledge_title"><span class="ttl1">いろは その<span>17</span></span> <span class="ttl2">車検当日に必要な持ち物は？</span></h5>
        <dl>
          <dt>車検当日に必要な持ち物は？</dt>
          <dd>
            <p><!--<img src="/img/user/knowledge/knowledge12.jpg" alt="車検をしている様子" width="557" /><br />-->
            初めての車検や久しぶりの車検だと当日に何を用意すればいいのか分からないですよね。用意し忘れて二度手間なんてことは避けたいです。この記事では皆さんが二度手間することのないように疑問や不安などを解決していきます。また、必要書類についてもご紹介していきます。</p>
          </dd>

          <dt>必ず準備するもの</dt>
          <dd>
            <p><span>自動車検査証</span></p>
              <p>車検証とは自動車が保安基準に合格していることを証明書です。また、所有者情報や車両情報なども記載してあります。車検や街頭検査など車両状態が検査証通りか確認する際に必要となります。<br />
              「いろは　その９」では車検証についてはもちろん、車検証をなくした場合などについても詳しく解説しています。<br />
              <a href="knowledge_9.php">→いろは9 車検証を紛失した場合はどうすればいいの？</a></p><br />
            <p><span>自動車納税証明書</span></p>
              <p>自動車納税証明書とは自動車税を納税していることを証明書です。<br />
              自動車税は毎年4月1日現在、自動車の所有者に5月ごろ公共料金の払い込み用紙である自動車納税通知書が送付されます。また6月までに支払いを完了する必要があります。自動車納税通知書をコンビニや金融機関に持参し自動車税を支払うと収納済印が押され返却されます。それが「納税証明書」となります。</p><br />
            <p><span>自賠責保険証明書</span></p>
              <p>自賠責保険証明書とは自賠責保険に加入していることを証明書です。<br />
              自賠責保険は車購入時に必ず加入する保険のことで強制保険とも呼ばれます。<br />
              紛失した場合は契約している保険代理店で事前に再発行を受けてください。再発行には数日かかる場合があります。</p><br />
            <p><span>印鑑・認印（シャチハタ不可）</span><br />
              <p>印鑑・認印は必ず必要ではありませんが業者によっては印鑑・認印を持参することをお願いされることがあるので持参するようにしてください。また車検証の使用者名義の印鑑・認印をご用意してください。</p>
                <br />
            <p><span>車検費用</span><br />
              <p>車検費用とは法定費用と車検基本料の総額のことで車検の際かかる費用のことです。<br />
              また車両や業者によって費用が異なってきます。<br />
              「いろは　　その７」では車検費用の内訳だけでなく車両別料金などについても解説しています。気になる方はぜひご確認ください。<br />
               <a href="knowledge_7.php">→いろは7 車検費用の内訳を徹底解説！</a></p>
                <br />
                <div class="button-wrap">
                  <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
                </div>
          </dd>


          <dt>必要かもしれないもの</dt>
          <dd>
            <p><span>ロックナットのアダプター　(ロックナットをしている車のみ)</span></p>
              <p>ロックナットをしている場合、車検の際ホイールナットを外すために専用のロックナットアダプターが必要です。ロックナットを付けている方は必ず用意してください。<br />
              また、つけていない方は必要ありません。<br />
              ※ロックナットとはホイールの盗難・紛失を防止するものです。</p><br />
            <p><span>任意保険の証券</span></p>
              <p>任意保険の証券とは自動車保険に加入していることを証明書です。基本的に車検では必要ありませんが業者によっては持参するよう指示があります。その際は特別な事情がない限り業者の指示に従ってください。</p><br />
                <!--<div class="button-wrap">
                  <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
                </div>-->
          </dd>

          <dt>ユーザー車検</dt>
          <dd>
           <p>ユーザー車検では「必ず用意するもの」でご紹介した5つに加え用意するものがあります。</p><br />
            <p><span>自動車重量税納付書</span></p>
              <p>自動車重量税納付書とは車検や構造等変更検査、新規登録検査の際に重量税（車税）を納付するための用紙です。陸運支局窓口でもらえます。</p><br />
            <p><span>自動車検査票</span></p>
              <p>自動車検査票は検査場の各検査を受検する際、合否を記録する用紙です。陸運支局窓口でもらえます。</p><br />
            <p><span>定期点検整備記録簿</span></p>
              <p>24ヶ月点検や12ヶ月点検の法定点検を行ったことを記録するための用紙です。整備手帳とも呼ばれます。購入時に車についていた定期点検記録簿を使用してください。</p><br />
            <p><span>継続検査申請書</span><br />
              <p>新しい車検証を発行する際に必要な用紙です。陸運支局窓口でもらえます。</p>
                <br />
                <div class="button-wrap">
                  <a href="#area_text" class="search_link_button">お住まいの地域から車検を探す</a>
                </div>
          </dd>

          <dt>その他</dt>
          <dd>
            <p><span>車検を受けず走行した場合</span></p>
              <p>車検を受けず公道を走行した場合法律違反になります。違反した場合厳しい罰則を受けることになります。<br />
              「車検　切れ」では罰則や有効期限が切れていた場合の対処法などについてご紹介しています。もし該当される方がいましたらご確認ください。<br />
              <a href="knowledge_13.php">→いろは13 車検切れの場合の対処法</a></p><br />
            <p><span>車検を受けるタイミング</span></p>
              <p>基本的に満了日１か月前から受けます。新車の場合は３年、それ以降は２年といった期間で車検を行います。<br />
              「いろは　その５」では有効期限の調べ方や期限が異なる車両についてご紹介しています。<br />
              <a href="knowledge_5.php">→いろは５ いつから受けられるの？車検の期間について</a></p><br />
            <p><span>定期点検整備記録簿</span></p>
              <p>車検の際には必要ありませんが、次回の車検までに車に備え付けておく義務があります。紛失した場合は指定の場所で再発行を受けてください。</p>
          </dd>

          <dt>まとめ</dt>
          <dd>
           <p>この記事では車検の際に必要なものについてご紹介しました。車検は２年に１度と何が必要なのか忘れてしまうと思います。今回、紹介した必要書類などを当日必ず持参して二度手間になるこのないようにしましょう。</p>
          </dd>
        </dl>

