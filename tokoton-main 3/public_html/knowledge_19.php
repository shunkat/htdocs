        <h5 id="knowledge_title"><span class="ttl1">いろは その<span>19</span></span> <span class="ttl2">1日車検ってなに？</span></h5>
        <dl>
          <dt>1日車検ってなに？</dt>
          <dd>
            <p><!--<img src="/img/user/knowledge/knowledge9_woman.jpg" alt="車検請求書" /><br />-->
              車検ってなるべく早く済ませたいですよね？整備工場などへ車を車検に出してしまうと、だいたい5営業日はかかってしまい、その間は慣れない代車で過ごさなければなりません。そこで今回、たった1日で車検を済ませる「1日車検」について詳しくご紹介します！
            </p>
          </dd>

          <dt>1日車検とは</dt>
          <dd>
　             <p><span>1日車検を受けられる場所</span></p>
              <div>
                <p>1日車検はカー用品店やガソリンスタンド、車検の更新に特化した車検専門業者などで受ける事ができます。ただ、全ての業者が1日車検を実施しているわけではないため注意が必要です。整備工場でも一部請け負っている業者もありますが、ディーラーは基本的には時間をかけた車検しかやっていないと考えたほうがいいでしょう。また、1日車検以外にも、もっと短い時間で済ませる60分車検、45分車検などもあります。「いろは その2」では45分車検・60分車検についてご紹介しています。<a href="http://www.tokoton-navi.com/knowledge_2.php">→いろは2 安心できる車検を選ぶコツ</a></p>

              </div>
              <br/>
              <p><span>1日車検でかかる時間</span></p>
              <div>
                <p>1日車検は、「当日一気に行う場合」と「事前に見積もりをする場合」の2パターンがあります。</p>

                <p><u>当日一気に行う場合</u>
                  <p>まず、当日一気に行う場合はその名の通り、1日で済ませることができます。朝早い時間帯に車を預け、夕方引き取るという流れになります。</p>
                <br />
                <p><u>事前に見積もりをする場合</u>
                  <p>一方、事前に見積もりをする場合、車検自体は1日で済ませることができるのですが、見積もりの日程も併せると、全体で最低2日かかります。
                <br />


              </div>
          </dd>


          <dt>1日車検の流れ</dt>
          <dd>
            <p>1日車検は素早く車検が終わりますが、その分1日にたくさんの内容が盛り込まれています。事前に流れを知っておき、よりスムーズに車検が受けられるようにしておきましょう。「当日一気に行う場合」と「事前に見積もりをする場合」に分けて説明します。</p>

  　            <p><span>当日一気に行う場合</span></p>
              <u>予約</u>
              <p>1日車検を受けるためには、まずは予約をお願いしましょう。予約の方法は業者によって異なりますが、電話やインターネットによる予約が多いです。必ず事前に予約を行っておきましょう。

                「いろは その11」では車検の予約についてはご紹介しています。<a href="http://www.tokoton-navi.com/knowledge_11.php">→いろは11 車検の予約ってどうすればいいの？</a></p>
              <br>
              <u>車の持ち込み、見積もり</u>
              <p>1日車検を受けるためには、まずは予約をお願いしましょう。予約の方法は業者によって異なりますが、電話やインターネットによる予約が多いです。必ず事前に予約を行っておきましょう。
                予約日当日に車を業者に持ち込み、その場でチェックを受けて車検にかかる費用など見積りを出してもらいます。業者によっては、この時点で料金が発生する場合もあるので注意が必要です。見積り額に問題がなければ、そのまま車を預けて検査を開始します。</p>
              <br/>
              <u>検査・精算</u>
              <p>車を預ければそこから検査開始となり、終了するのは夕方頃です。検査が終わった車は自分で引き取りに行き、検査で異常が無ければ精算となります。精算をすれば車を引き取って車検は終了です。</p>
  　            <p><span>事前に見積もりをする場合</span></p>
  <p>見積もりの予約を取り、その後車の状態を見て、詳細なスケジュールの確認を行います。その後の流れは「当日一気に行う場合」と同様です。

    「いろは その10」見積もりの予約についてご紹介しています。

    <a href="http://www.tokoton-navi.com/knowledge_10.php">→いろは10 車検の見積もりの方法とメリット</a></p>
  <br>
</dd>


  <dt>1日車検の費用</dt>
  <dd>
    <p>1日車検は点検・整備作業が効率化しているため、費用を安く抑えられます。
    </p>
    <p><span>費用の目安</span></p>
      <p>通常の車検では利用する業者によっても異なりますが、10万円近くかかってしまうことも多いですが、1日車検にかかる費用は、業者の手数料だけで考えるなら1～3万円程度です。ただ、車の状態や業者が提示する基本料金によって変わり、車ごと、業者ごとに差が大きいため注意が必要です。ここに整備の費用や法定費用がプラスされるため、5～7万円程度は予算を見ておきましょう。また、異常があった場合、部品の交換等もしているのでそれらの費用もプラスされます。軽自動車で法定費用が安い場合や整備がほとんど不要な保安基準に適合している状態であれば、最低限のコストで済みます。
      </p>
      <br>
      <p><span>1日車検にかかる費用の出し方</span></p>
      <p>1日車検費用の出し方は以下の通りです。
      </p>
      <div style="border:solid 2px black; text-align:center;">1日車検費用＝基本検査費用＋整備代行料＋検査更新料＋法定検査費用</div>

      <p>この費用の中で「基本検査費用」「整備代行料」「検査更新料」は車検代行業者により価格が変わります。どの代行業者もインターネット等で見積りを取れるので何社か見積りをとって比較して見るといいでしょう。いろは その7」では車検費用の詳細についてご紹介しています。
        <a href="http://www.tokoton-navi.com/knowledge_7.php">→いろは7 車検費用の内訳を徹底解説！</a>
      </p>
      </dd>

      <dt>1日車検のメリット・デメリット</dt>
      <dd>

        <p><span>1日車検のメリット</span></p>
        <u>時間が短い</u>
          <p>最大のメリットは1日で素早く終わることです。時間がかからないため代車を借りる必要もありません。 特に車検専門のフランチャイズの整備工場なら、整備士の数も多く車検に特化しているため、時間を短縮できる可能性がぐんと高まり、3時間程度で車検を完了させられることもあります。
          </p>
          <br>
          <u>費用が安い</u>
          <p>1日車検では、整備や点検の作業を徹底的に効率化されているため、通常の車検より費用が安く設定されており、３〜5万円お得です。また、車の状態を点検した直後に必要な整備内容を説明してもらえることも多いため、費用への納得感が得やすいでしょう。
          </p>
          <br>
          <p><span>一日車検のデメリット</span></p>
          <u>整備は最低限</u>
          <p>1日車検では業者が車を預かる時間が少ない分、短時間でできる、最低限の検査しかおこなわれません。そのため、車の安全性については疑問が残る場合もあります。車検は本当に安全かどうかを確かめる必要があるので、検査内容を考えれば通常の車検の方がしっかりしています。
          </p>
          <br>
          <u>車検に出す前に車を整備しておかなければならない</u>
          <p>1日車検でも簡単な整備は業者でやってくれますが、メンテナンス可能なのはごく些細なことに限られます。そのため、基本的には車検に出す前に自身で車を整備しておく必要があります。状態をある程度整えておかなければならないため、事前準備に苦労しやすく、時間や費用がかかることも多いです。</p>
          </dd>


          <dt>1日車検での注意点、車検業者を探す方法</dt>
          <dd>
            <p><span>注意点</span></p>
            <p>1日車検でも車検が1日以上かかることがあります。1日で車検を終わらせる事が出来るのは、あくまでも予定通りに持ち込みや点検・整備が完了した場合のみで、それ以外のケースでは車検の完了が伸びる事もあります。具体的には、点検の際に不具合が見つかり、その交換に使う部品が在庫切れを起こしている場合や業者に預けてから不具合が見つかり、それがすぐに直せない場合です。新車であればこういった事にはならないでしょうが、車購入から月日が経っていてそれなりの距離を走っている車となると部品交換の可能性も高くなります。

            </p><br>
            <p><span>車検業者を探す方法</span></p>
            <p>やはり車のメンテナンスを本業とする業者の1日車検が良いでしょう。</p><br>
            <p>具体的にいえば車検専門フランチャイズがベストだといえます。</p><br>
            <p>また、1日車検にするか 短時間車検にするかで迷った際には車の年数とエンジン異音の有無で決めるべきです。エンジン異音があればディーラーでの車検、それ以外は60分や1日車検で十分です。エンジンというのは車の本質であるため、ここに異音がしていれば年数が1年でもすぐにしっかりと点検整備をする必要があります。平均的な走行距離として1年で1万キロと言われているため、10年では10万キロになります。通常のドライバーはこの期間で買い替えを考えるような故障もあまり経験しないでしょう。そのため10年目までの車の場合、整備工場や60分車検（フランチャイズ）で十分です。

            </p>
          </dd>

          <dt>短時間で車検を済ませたいなら1日車検がおすすめ

          </dt>
          <dd>
