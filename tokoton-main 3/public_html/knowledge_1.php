       <h5 id="knowledge_title"><span class="ttl1">いろは その<span>1</span></span> <span class="ttl2">車検って何？</span></h5>
        <dl>
          <dt>車検とは</dt>
          <dd>
          道路運送車両法では、車が安全性や公害防止面で問題ないかどうかを国の機関（運輸支局）で定期的に検査するよう義務づけられています。これを正式には<span>「継続検査」</span>、一般名称では<strong>「車検」</strong>といいます。車検の有効期限が切れた自動車は、原則的に公道を走ることが認められていません。これに違反してしまうと、免停などを含めた厳しい罰則の対象となります</dd>
          <dt>車検の有効期間</dt>
          <dd>車検を行う時期に関して、自家用乗用車と軽乗用自動車は、新車登録から3年目、それ以後は<span>2年ごと</span>と定められています。<br>
          （トラックやバスなど事業用自動車の期間はさらに短く定められています。）<br>
          車検の有効期間を車検満了日と言い、自動車検査証（車検証）に記載されています。<br>
          車検に合格すると、この車検証が更新されて、新しい車検満了日が記載された<span>検査標章</span>（フロントウィンドウに貼るシール、通称「車検ステッカー」）がもらえます。<br>
          車検満了日を過ぎた自動車で公道を走ると重い処分（免許停止90日）の対象になります。<br>
           <p>車検の有効期間について詳しくは<a href="http://www.tokoton-navi.com/sp/knowledge/knowledge_5.php">"いつから受けられる？車検の期間"</a>をご覧ください。</p>
