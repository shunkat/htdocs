          <dt>車検の時に行うこと</dt>
          <dd>
            車検の前または後に<span>法定2年点検</span>を実施し、必要に応じて整備を行わなければなりません。<br>
            したがって一般的には、「点検」「整備」「検査」を総称して<strong>「車検」</strong>と呼ばれています。<br />
            また、車検を行った際には、同時に<span>自賠責保険料</span>と<span>重量税</span>を支払う必要があります。
            </dd>
