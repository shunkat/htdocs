          <dt>車検に必要なもの</dt>
          <dd>車検当日に必要なものは以下の通りです。特に「<span>自動車税納税証明書</span>」は税務署から自宅に郵送されてくる書類なので、紛失してしまう方が多いようです。しっかりと保管をしておき、車検当日に困ってしまわないようお気を付けください。<br />
