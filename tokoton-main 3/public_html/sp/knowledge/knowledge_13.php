            <h2 class="shop-info-sub"><span class="shop-info-sub-in">車検切れのときの対処の方法その２　車を手放す場合</span></h2>
            <p><span class="shop-info-sub-in">廃車にする</span></p>
            <p>実は車検切れの車も廃車にすることが可能です。一時的に車を使用せず、放置しておくだけの場合や今の車を買ってから10年以上経つ場合、車が故障している場合は、廃車として処分してしまうのも1つの手です。<br>
            車検切れの車を放置しているだけでも税金はかかってしまいますし、車の寿命は10年ほどといわれており、それを超えると、たとえ修理をしたとしても故障のリスクは高まるという理由があります。また、修理すると上乗せされたお金がかかってしまうという理由もあります。<br>
            それでは、車検切れの車を廃車にする手順はどのようになっているでしょうか？その内容を見ていきましょう。<br>
            <br>
            まず、解体業者に廃車にする車を解体してもらいます。この時に注意しなければならないことは、解体業者に車を運ぶ際も車検切れの車を公道で走行させることができません。そのため、上記の「車検切れのときの対処の方法1」で挙げた、役所で仮ナンバーを所得するか、業者を手配し、レッカー車で運んでもらうことを検討しましょう。<br>
            車を解体した後に、普通自動車の場合は陸運局という役所で、軽自動車の場合は軽自動車検査協会で廃車手続きを行います。<br>
            ただし、車検証がなければ廃車にすることができません。車検証は再発行することが可能です。必要書類をもって役所に行きましょう。<br>
            車検証の再発行手続きは廃車手続きと同様に、普通自動車の場合は陸運局という役所で、軽自動車の場合は自動車検査協会で行います。<br>
            車検の紛失について、詳しくはこちらをご覧ください。<br>
            <a href="knowledge_9.php">車検のいろは その9「車検証を紛失した場合はどうすればいいの？」)</a></p>
            <p><span class="shop-info-sub-in">売却する</span></p>
            <p>車検切れの車でも、中古車販売店などに売却することができます。車検が切れる前から売却を考えていたのであれば、売ってしまうという選択肢があることも念頭に入れておきましょう。<br>
            その場合は買取金額の査定を事前に見積もりましょう。</p>

            <h2 class="shop-info-sub"><span class="shop-info-sub-in">車検切れの取り締まりが厳しくなっている</span></h2>
            <p><span class="shop-info-sub-in">ナンバー自動読み取り装置による取り締まり</span></p>
            <p>ナンバー自動読取装置とは、日本の道路に設置されているシステムで、走行中の自動車のナンバープレートを自動的に読み取り、車検切れかどうかを照合することができます。<br>
            国土交通省によると、2018年9月から2019年3月まで可搬式の「ナンバー自動読取装置」を本格導入した街頭検査を全国35都道府県で実施し、車検切れ運行車両43台を捕捉したという状況です。<br>
            今後も装置を活用して車検切れの車への取り締まりが強化されていく状況になると考えられます。車検が切れていてもばれないのでは？と思う人がいるかもしれませんが、きちんと車検に出しましょう。</p>


            <h2 class="shop-info-sub"><span class="shop-info-sub-in">まとめ</span></h2>
            <p>今回は、車検が切れたときの罰則やその対処法についてまとめました。車検が切れた自動車の走行は免許停止処分を含む罰則や損害賠償など、大きなリスクや大変無駄なコスト、手間がかかってしまう可能性があります。そして、年々取り締まりも強化されてきています。車検切れのタイミングは早めのうちから意識しましょう。できれば期限内に、万が一過ぎてしまったとしても紹介した対処の内容で情報を確認し、きちんと車検を行うことが大切です。安心安全なクルマライフを送りましょう！</p>
