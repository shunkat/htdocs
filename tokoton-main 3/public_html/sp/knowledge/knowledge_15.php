            <h2 class="shop-info-sub"><span class="shop-info-sub-in">エコカー減免とは？どう変わる？</span></h2>
            <p><span class="shop-info-sub-in">エコカー減税とは？</span></p>
            <p>エコカー減税とは国土交通省が一定の燃焼基準をクリアした車をエコカーと認定します。<br>
            環境性能に優れた車に対する自動車税、自動車重量税などが軽減される特例措置です。<br>
            基準達成率ごとに25％、50％、75％、100％と税金を安くすることが出来ます。</p>
            <p><span class="shop-info-sub-in">対象車種は？</span></p>
            <p>低燃費・低排出な車ならグリーンディーゼルや通常のハイブリットさらにガソリン車も対象です。電気自動車やＰＨＶハイブリットなどの次世代車種だけが対象ではないです。</p>
            <p><span class="shop-info-sub-in">適用期間</span></p>
            <p>重量税：2019年5月1日～2021年4月30日までに新車登録及び最初の車検を受ける場合<br>
            自動車税：2021年3月31日までに新車登録者対象</p>
