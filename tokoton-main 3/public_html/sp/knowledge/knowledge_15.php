            <h2 class="shop-info-sub"><span class="shop-info-sub-in">車検代の相場は？</span></h2>
            <p><span class="shop-info-sub-in">車検とは？</span></p>
            <p>車検は所有している車が安全面や環境面が国の定めた基準をクリアしているかチェックする検査のことです。自動車車検証には有効期限があります。期間は初回の新車購入後3年、それ以降は2年ごとです。そのため、定期的に車検を受ける必要があります。<br>
            車検は検査証の有効期限満了日の1か月前から受けることが可能です。<br>
            <a href="knowledge_1.php">詳しくは「いろは その１」へ</a></p>
            <p><span class="shop-info-sub-in">車検費用はどう決まる？</span></p>
            <p>車検費用は車検基本料と法定費用の総額です。<br>
            車検基本料：整備や代行など業者のサービスであり業者ごとに値段が異なります。<br>
            法定費用：国に支払うお金なので全国一律の金額です。（重量税・自賠責保険料・印紙税）車種別や13年経過車、エコカーの減免によって値段が変動します。<br>
            24ヶ月点検：車検とは別で部品を交換する際にかかる費用。</p>
