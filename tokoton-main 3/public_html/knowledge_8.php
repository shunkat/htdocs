          </dd>
          <dt>実は民間車検でも整備の質は変わらない！？</dt>
          <dd>ディーラーに車検を頼んでも、町の整備工場に外注をしていることも少なくありません。その証拠に、車検シールや明細書に記載されている店舗名を見ると依頼したディーラーの店舗と異なる工場名が書かれていることがあります。<br>
<br>
というのも車検業者には、陸運支局の代わりに車検を行うことが許可されている「指定工場」と、点検や整備・修理はできるものの自社の検査ラインを持っていない「認証工場」の2種類があります。認証工場に持ち込んだ場合、検査の際には陸運支局に持っていかなくてはなりません。<br>
<br>
ディーラー車検に依頼をしても、そこが指定工場でなければ別のディーラー店舗や下請けの整備工場に回してしまうことがあったり、指定工場であったとしても工場がキャパオーバーで新たに車を受け入れられないことがあったりするため、このようなことが起こります。<br><span>つまり、窓口はディーラーでありながら実際に整備を行うのは町の整備工場であるというパターンもある</span>のです。<br>
<br>
ディーラー車検で申し込みをした場合、まだ使えるパーツを交換したり、不要な整備をしたりすることによって高額になっている可能性もあります。「今まで車検はディーラーしか考えたことはない！」という方も、一度ディーラー車検と民間車検をそれぞれ見積もって、整備内容を比較してみましょう。
